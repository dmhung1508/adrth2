        categoryCounter.put("√Åo", 0);
        categoryCounter.put("Qu·∫ßn", 0);
        categoryCounter.put("Ph·ª• ki·ªán", 0);

        // Th√™m d·ªØ li·ªáu m·∫´u
        addSampleData();

