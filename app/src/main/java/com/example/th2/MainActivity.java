        stats.append("üì¶ T·ªïng s·ªë s·∫£n ph·∫©m: ").append(totalProducts).append("\n\n");
        stats.append("üëï S·ªë l∆∞·ª£ng √Åo: ").append(aoCount).append("\n");
        stats.append("üëñ S·ªë l∆∞·ª£ng Qu·∫ßn: ").append(quanCount).append("\n");
        stats.append("üëú S·ªë l∆∞·ª£ng Ph·ª• ki·ªán: ").append(phukienCount).append("\n\n");
        
